class Ai1wmde_Import_Database {

	public static function execute( $params ) {

		// Set progress
		Ai1wm_Status::info( __( 'Updating Dropbox settings...', AI1WMDE_PLUGIN_NAME ) );

		// Set refresh token
		update_option( 'ai1wmde_dropbox_token', $params['dropbox_token'] );

		// Set SSL mode
		update_option( 'ai1wmde_dropbox_ssl', $params['dropbox_ssl'] );

		// Unset refresh token
		unset( $params['dropbox_token'] );

		// Unset SSL mode
		unset( $params['dropbox_ssl'] );

		// Set progress
		Ai1wm_Status::info( __( 'Done updating Dropbox settings.', AI1WMDE_PLUGIN_NAME ) );

		return $params;
	}
}
